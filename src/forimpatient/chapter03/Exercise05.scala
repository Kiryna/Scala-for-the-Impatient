  * Solution to the Chapter 03 Exercise 05 'Scala for the Impatient' by Horstmann C.S.
  *
  * How do you compute the average of an Array[Double]?
  */
object Exercise05 extends App {
  println("Chapter 03 Exercise 05")
  val a = Array(1.2, 5.7, 4.0, -8.4, 3.8, -1.3, 3.9, 1.4, -5.7, 8.4)

  println(a)
  println(a.sum / a.length)
}
